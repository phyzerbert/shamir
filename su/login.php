        function vanillaCarousel(carouselName) {
            //console.log("inside8");
            var CAROUSEL            = document.getElementById(carouselName),
                CAROUSELNAV         = CAROUSEL.getElementsByClassName('vanilla-carousel--tab-control'),
                CAROUSELPLAY        = CAROUSEL.getElementsByClassName('vanilla-carousel--speed')[0],
                CAROUSELCONTENT     = CAROUSEL.getElementsByClassName('vanilla-carousel--slide'),
                CAROUSELCONTROLS    = CAROUSEL.getElementsByClassName('vanilla-carousel--controls')[0],
                screenReaderText    = CAROUSEL.getElementsByClassName('speed-control--label')[0];
            // Keybinding
            var keys = {
                end: 35,
                home: 36,
                left: 37,
                up: 38,
                right: 39,
                down: 40,
                delete: 46,
                shift: 16,
                tab: 9,
                enter: 13
            };
            // Map the direction of the focus to the key that is hit.
            var direction = {
                37: -1,
                38: -1,
                39: 1,
                40: 1
            };
            // Touch detection
            var touchstartX = 0;
            var touchstartY = 0;
            var touchendX = 0;
            var touchendY = 0;
            var gesuredZone = document.getElementById('gesuredZone');
            // Touch event listeners for touch devices
            CAROUSEL.addEventListener('touchstart', function(event) {
                touchstartX = event.screenX;
                touchstartY = event.screenY;
            }, false);
            CAROUSEL.addEventListener('touchend', function(event) {
                touchendX = event.screenX;
                touchendY = event.screenY;
                handleGesure();
            }, false); 
            // Mouse event listeners for mouse devices
            CAROUSEL.addEventListener('mousedown', function(event) {
                touchstartX = event.screenX;
                touchstartY = event.screenY;
            }, false);
            CAROUSEL.addEventListener('mouseup', function(event) {
                touchendX = event.screenX;
                touchendY = event.screenY;
                handleGesure();
            }, false); 
            function handleGesure() {
                var swiped = 'swiped: ';
                if (touchendX < touchstartX) {
                    tabFocusOnArrow(-1, false);
                    autoPlay(0);
                }
                if (touchendX > touchstartX) {
                    tabFocusOnArrow(1, false);
                    autoPlay(0);
                }
                if (touchendY < touchstartY) {
                    autoPlay(0);
                    // console.log(swiped + 'down!');
                }
                if (touchendY > touchstartY) {
                    autoPlay(0);
                    // console.log(swiped + 'left!');
                }
                if (touchendY == touchstartY) {
                    autoPlay(0);
                    // console.log('tap!');
                }
            }
            function generateArrays () {
                TABARRAY = CAROUSEL.querySelectorAll('[role="tab"]');
                PANELARRAY = CAROUSEL.querySelectorAll('[role="tabpanel"]');
                CAROUSELARROWS = CAROUSEL.querySelectorAll('.vanilla-carousel--tab-arrows');
            };
            function addListeners (index) {
                TABARRAY[index].addEventListener('click', clickEventListener);
                TABARRAY[index].addEventListener('keydown', keydownEventListener);
                TABARRAY[index].addEventListener('focus', focusEventListener);
                CAROUSELPLAY.addEventListener('focus', focusEventListener);
                // Build an array with all tabs (<button>s) in it
                TABARRAY[index].index = index;
            };
            function focusEventListener(event) {
                autoPlay(0, false);
            }
            function clickEventListener (event) {
                var tab = event.target;
                var currentSlide = CAROUSEL.getElementsByClassName('active')[0].dataset.customslide;
                resetClasses(tab, true);
                carouselSlide(CAROUSEL.querySelector('#'+currentSlide),  CAROUSEL.querySelector('#'+tab.dataset.customslide));
            };
            function carouselArrowNavigation(){
                CAROUSELARROWS[0].addEventListener('click', function(){
                    tabFocusOnArrow(-1, false);
                });
                CAROUSELARROWS[1].addEventListener('click', function(){
                    tabFocusOnArrow(1, false);
                });
            };
            // activeClass is the target of whatever is being clicked and needs the new active class
            function resetClasses(activeClass, setFocus){
                [].map.call(CAROUSEL.querySelectorAll('.vanilla-carousel--tab .vanilla-carousel--tab-control'), function(el){
                    el.classList.remove('active');
                    el.setAttribute("aria-selected", "false");
                    el.setAttribute('tabindex', "-1");
                });
                activeClass.classList.add('active');
                activeClass.setAttribute("tabindex", "0");
                if (setFocus === true){
                    activeClass.setAttribute("aria-selected", "true");    
                    document.getElementById(activeClass.id).focus();
                };
            };
            function keydownEventListener(event){
                var key = event.keyCode;
                switch (key) {
                    case keys.end:
                        event.preventDefault();
                        activateTab(TABARRAY[TABARRAY.length - 1]);
                        break;
                    case keys.home:
                        event.preventDefault();
                        activateTab(TABARRAY[0]);
                        break;
                    case keys.left:
                        event.preventDefault();
                        tabFocusOnArrow(-1, true);
                        break;
                    case keys.up:
                        event.preventDefault();
                        tabFocusOnArrow(-1, true);
                        break;
                    case keys.right:
                        event.preventDefault();
                        tabFocusOnArrow(1, true);
                        break;
                    case keys.down:
                        event.preventDefault();
                        tabFocusOnArrow(1, true);
                        break;
                    case keys.tab:
                        // event.preventDefault();
                        // tabFocusOnArrow('tab');
                        break;
                    case keys.enter:
                        // console.log("prevent enter key from breaking in screen readers");
                        event.preventDefault();
                        // tabFocusOnArrow('tab');
                        break;
                    // case (keys.shift && keys.tab):
                    //     event.preventDefault();
                    //     console.log('shift tab');
                    //     break;
                }
            }
            function tabFocusOnArrow(event, updateFocus){
                var currentActiveElement = "this";
                var nextActiveElement = "that";
                // Loop through all the tabs.
                for(i = 0; i < TABARRAY.length; i++) {
                    // See which tab is the active tab.
                    if (TABARRAY[i].classList.contains('active')){
                        // If the element is the first item and the user presses back.
                        if ( i === 0 && event === -1 ) {
                            var currentActiveElement = TABARRAY[i];
                            var nextActiveElement = TABARRAY[TABARRAY.length - 1];
                        }
                        // If the element is the last item and the user presses forward.
                        else if ( i === TABARRAY.length - 1 && event === 1 ) {
                            var currentActiveElement = TABARRAY[i];
                            var nextActiveElement = TABARRAY[0];
                        }
                        // Just normal
                        else {
                            var currentActiveElement = TABARRAY[i];
                            var nextActiveElement = TABARRAY[i + event];
                        }
                    }
                }
                // Reset the classes.
                resetClasses(nextActiveElement, updateFocus);
                // Animate the carousel slide.
                carouselSlide(CAROUSEL.querySelector('#'+currentActiveElement.dataset.customslide), CAROUSEL.querySelector('#'+nextActiveElement.dataset.customslide));
            }
            function activateTab(event){
                var currentSlide = CAROUSEL.getElementsByClassName('active')[0].dataset.customslide;
                var nextSlide = event.dataset.customslide;
                resetClasses(event, false);
                carouselSlide(CAROUSEL.querySelector('#'+currentSlide), CAROUSEL.querySelector('#'+nextSlide));
            }
            function initializeCarousel(){
                // remove the nojavascript class  when javascript loads.
                CAROUSELCONTROLS.classList.remove('nojavascript');
                generateArrays();
                playControls();
                carouselControls();
                carouselArrowNavigation();
                // On initialize of large screens, initialize the carousel.
                if ( document.documentElement.clientWidth > 992) {
                    playCarousel();
                }
                // Add the event listeners to all of the tabs.
                for (i = 0; i < TABARRAY.length; ++i) {
                    addListeners(i);
                };
            };
            function carouselSlide(currentSlide, nextSlide){
                //TNT AJ:Start
                for(i = 0; i < TABARRAY.length; i++) {
                    // See which tab is the active tab.
                    if (TABARRAY[i].classList.contains('active')){
                        var temp = parseInt(localStorage.getItem('banner'+(i+1)+'imp'));
                        localStorage.setItem('banner'+(i+1)+'imp',++temp);
                    }       
                }
                //TNT AJ:Close
                currentSlide.setAttribute("hidden", "hidden");
                currentSlide.setAttribute("style","display:none");
                currentSlide.setAttribute("tabindex", "-1");
                nextSlide.setAttribute("style","display:block");
                nextSlide.removeAttribute("hidden");
                nextSlide.setAttribute("tabindex", "0");
            }
            // This might be a dead function -- all it really does right now is initialize the carousel controls and give it an active class
            function carouselControls(){
                CAROUSELCONTROLS.classList.add('initialized');
                CAROUSEL.getElementsByClassName('vanilla-carousel--svg-animation')[0].classList.add('animate-in');
                var counter = 0;
                // Animate the bubbles in on page load
                function fadeBubbles(){
                    TABARRAY[counter].classList.add('transition-hidden');
                    counter ++;
                    if (counter < TABARRAY.length){
                        setTimeout(fadeBubbles, 300);
                    }
                }
                fadeBubbles();
            }
            function playControls(){
                // toggles the action on click
                CAROUSELPLAY.onclick = function(){
                    // Grab the data-action value
                    PLAYVALUE = CAROUSELPLAY.dataset.action;
                    // If data-action is equal to pause, sw
                    if ( PLAYVALUE === 'play') {
                        pauseCarousel();
                    }
                    else if ( PLAYVALUE === 'pause') {
                        playCarousel();
                    }
                }
            }
            var duration;
            function autoPlay(play) {
                // If play = 1 autoplay
                // If play = 0 pause
                if (play === 1){
                    duration = setInterval(function(){
                        tabFocusOnArrow(1, false)
                    }, 5000);
                }
                else if (play === 0) {
                    // Clear the timeout on the duration function above. This prevents the carousel from firing the duration function multiple times per second.
                    clearInterval(duration);
                }
            }
            // Pause the carousel function!
            function pauseCarousel(){
                CAROUSELPLAY.setAttribute("data-action", "pause");
                CAROUSELPLAY.classList.toggle('pause');
                CAROUSELPLAY.setAttribute('aria-label', "Play slideshow")
                autoPlay(0);
            }
            // Play the carousel function!
            function playCarousel(){
                CAROUSELPLAY.setAttribute("data-action", "play");
                CAROUSELPLAY.setAttribute('aria-label', "Pause slideshow")
                CAROUSELPLAY.classList.toggle('pause');
                autoPlay(1);
            }
            // When a user focuses on the window -- auto play the carousel.
            // window.onfocus = function(){
            //     playCarousel();
            // }
            // When a user tabs away from the window -- pause the carousel.
            window.onblur = function(){
                pauseCarousel();
            }
            // Fire the initalize carousel function
            initializeCarousel();
        }
